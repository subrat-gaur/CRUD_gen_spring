CREATE TABLE aircrafts (id SERIAL PRIMARY KEY, aircraftName VARCHAR(255), 
                                           aircraftOwner VARCHAR(255),
                                           engineManufacturer VARCHAR(255),
                                           engineModel VARCHAR(255),
                                           liveryCode VARCHAR(255),
                                           ownershipTypeCode VARCHAR(255),
                                           registrationNumber VARCHAR(255),
                                           remarks VARCHAR(255),
                                           serialNumber VARCHAR(255),
                                           typeIATACode VARCHAR(255),
                                           AircraftWeight VARCHAR(255)
                                           );

